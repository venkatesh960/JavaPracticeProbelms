//Multiply Two Floating-Point Numbers
import java.util.*; 
class MulofFloat {
    public static void main(String[] args)
    {
       Scanner obj=new Scanner(System.in);
       float x=obj.nextFloat();
       float y=obj.nextFloat();
      float z=x*y;
       System.out.println(z);
       obj.close();
    }
    
}
